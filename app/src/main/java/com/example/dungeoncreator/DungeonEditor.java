    private void debug__printGrid(int[][] grid) {
        String gridLine = "";
        for (int i = 0; i < grid.length;i++) {
            for (int q = 0; q < grid[i].length;q++) {
                gridLine += " "+grid[i][q];
                //gridLine += " "+q;
            }
            Log.d("Grid "+i,gridLine);
            gridLine = "";
        }
    }



