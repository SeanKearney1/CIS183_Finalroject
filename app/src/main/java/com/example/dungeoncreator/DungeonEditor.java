    private void FillSpinners(boolean IsUpdating) {
        ArrayAdapter<String> spinnerAdapter1 = new ArrayAdapter<>(this,android.R.layout.simple_spinner_item,BrushModes);
        ArrayAdapter<String> spinnerAdapter2 = new ArrayAdapter<>(this,android.R.layout.simple_spinner_item,BrushItems[sp_brushMode.getSelectedItemPosition()]);

        if (!IsUpdating) {
            sp_brushMode.setAdapter(spinnerAdapter1);
        }
        sp_brushItem.setAdapter(spinnerAdapter2);
    }










    private void Move(boolean IsX, int direction) {
        //-1 is Down and Right.
        if (IsX) {
            directionX += directionSize*direction;
            if (directionX > directionMax) { directionX = directionMax; }
        }
        else {
            directionY += directionSize*direction;
            if (directionY > directionMax) { directionY = directionMax; }
        }
        fillTiles();
    }
    private void Zoom(boolean IsZoomIn) {
        // this was switched because how the math works,
        // so 5(max) is fully zoomed out, and 1 is fully zoomed in.
        if (!IsZoomIn) {
            if (zoomLevel > 1) {
                zoomLevel--;
            }
        }
        else {
            if (zoomLevel < zoomMax) {
                zoomLevel++;
            }
        }
    }






    private void fillTiles() {




        gv_grid.setNumColumns(DungeonData.getDungeonSize());
        gv_grid.setColumnWidth(36);

        gv_grid.setScaleX(zoomLevel);
        gv_grid.setScaleY(zoomLevel);

        GVAdapter = new GridViewEditor(this, dungeon, (int)Math.pow(2,zoomLevel));
        gv_grid.setAdapter(GVAdapter);


        gv_grid.setPadding(directionX,directionY,0,0);

    }













    private void brush(int position) {
        //{"Eraser", "Tiles", "Objects", "Enemies", "Items"};
        int posY = position/dungeon.getTiles().length;
        int posX = position%dungeon.getTiles()[0].length;


        if (sp_brushMode.getSelectedItemPosition() == 0) {
            if (dungeon.getEnemies()[posY][posX] != 0)      { dungeon.getEnemies()[posY][posX] = 0; }
            else if (dungeon.getItems()[posY][posX] != 0)   { dungeon.getItems()[posY][posX] = 0; }
            else if (dungeon.getObjects()[posY][posX] != 0) { dungeon.getObjects()[posY][posX] = 0; }
            else                                            { dungeon.getTiles()[posY][posX] = 0; }
        }
        else if (sp_brushMode.getSelectedItemPosition() == 1) {
            dungeon.getTiles()[posY][posX] = sp_brushItem.getSelectedItemPosition()+1;
        }
        else if (sp_brushMode.getSelectedItemPosition() == 2) {
            dungeon.getObjects()[posY][posX] = sp_brushItem.getSelectedItemPosition()+1;
        }
        else if (sp_brushMode.getSelectedItemPosition() == 3) {
            dungeon.getEnemies()[posY][posX] = sp_brushItem.getSelectedItemPosition()+1;
        }
        else if (sp_brushMode.getSelectedItemPosition() == 4) {
            dungeon.getItems()[posY][posX] = sp_brushItem.getSelectedItemPosition()+1;
        }
    }











